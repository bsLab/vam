open Amoeba
open Dir

let name_lookup name =
    dir_lookup ~root:nilcap ~name:name


let name_append ~name:name ~cap:cap =
    dir_append ~root:nilcap ~name:name ~obj:cap            

let name_delete name =
    dir_delete ~root:nilcap ~name:name  
